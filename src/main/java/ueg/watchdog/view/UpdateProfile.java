    public void loadUpdateProfile() {
        imagePaths.addAll(profile.getImagePaths());

        repaintProfileImages();

        jTextFieldId.setText(profile.getPersonalId());
        jTextFieldFirstName.setText(profile.getFirstName());
        jTextFieldLastName.setText(profile.getLastName());
        jTextFieldContact.setText(profile.getContact());
        jTextFieldAddress.setText(profile.getAddress());

        if (profile.getGender().equals("Male")) {
            jRadioButtonMale.setSelected(true);
            jRadioButtonFemale.setSelected(false);
        } else {
            jRadioButtonFemale.setSelected(true);
            jRadioButtonMale.setSelected(false);
        }

        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(profile.getDob());
            jDateChooserDob.setDate(date);
        } catch (ParseException ex) {
            logger.error("error when parsing date", ex);
        }
        JTextFieldDateEditor editor = (JTextFieldDateEditor) jDateChooserDob.getDateEditor();
        editor.setEditable(false);
    }

    /**
     * Method to redraw all images currently have been added to the UI
     */
    private void repaintProfileImages() {
        imagePanel.removeAll();

        int width = imagePanel.getWidth() / 3;
        int height = imagePanel.getHeight() / 3;

        for (String path : imagePaths) {
            JLabel imageLabel = new JLabel(ImageUtils.resizeImage(path, width, height));
            imageLabel.setSize(width, height);
            imagePanel.add(imageLabel);
        }
        imagePanel.validate();
        imagePanel.repaint();
    }

