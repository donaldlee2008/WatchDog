    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        fc.addChoosableFileFilter(filter);
        int result = fc.showSaveDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fc.getSelectedFile();
            if (imagePaths.contains(selectedFile.getAbsolutePath())) {
                logger.warn("Image file already exists");
                return;
            }
            imagePaths.add(selectedFile.getAbsolutePath());

            repaintProfileImages();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String profId = jTextFieldId.getText();
        String firstName = jTextFieldFirstName.getText();
        String lastName = jTextFieldLastName.getText();
        String dob = ((JTextField) jDateChooserDob.getDateEditor().getUiComponent()).getText();
        String contact = jTextFieldContact.getText();
        String address = jTextFieldAddress.getText();

        String gender;
        if (jRadioButtonMale.isSelected()) {
            gender = jRadioButtonMale.getText();
        } else {
            gender = jRadioButtonFemale.getText();
        }

        if (profId.equals("") || firstName.equals("") || lastName.equals("") || dob.equals("") || contact.equals("") || address.equals("") || gender.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill all the input fields!", "Failed!", 0);
        } else if (!Validator.validateInputs(profId, 3)) {
            JOptionPane.showMessageDialog(null, "Personal ID is not matching the required format!", "Failed!", 0);
        } else if (!Validator.validateInputs(dob, 0)) {
            JOptionPane.showMessageDialog(null, "DOB is not matching the required format!", "Failed!", 0);
        } else if (Validator.validateInputs(dob, 1)) {
            JOptionPane.showMessageDialog(null, "DOB can not be a future date!", "Failed!", 0);
        } else if (!Validator.validateInputs(contact, 2)) {
            JOptionPane.showMessageDialog(null, "Contact Number is not matching the required format!", "Failed!", 0);
        } else {
            profile.setPersonalId(profId);
            profile.setFirstName(firstName);
            profile.setLastName(lastName);
            profile.setDob(dob);
            profile.setGender(gender);
            profile.setContact(contact);
            profile.setAdress(address);
            profile.setImagePaths(imagePaths);

            if (Profile.updateUserProfile(profile)) {
                JOptionPane.showMessageDialog(null, "User Profile Updated Successfully!", "Success!", 1);
                this.openGivenWindow(new MainWindow());
                //this.exitToParent(false);
            } else {
                JOptionPane.showMessageDialog(null, "Failed! Please try again or exit!", "Fail!", 0);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

